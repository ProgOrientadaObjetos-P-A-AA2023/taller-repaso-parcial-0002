package paquete002;

public class Propiedad {

    public double costoPropiedad;

    public Propiedad(double cP) {
        costoPropiedad = cP;
    }

    public double obtenerCostoPropiedad() {
        return costoPropiedad;
    }

    public void establecerCostoPropiedad(double cP) {
        costoPropiedad = cP;
    }

}
